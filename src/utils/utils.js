export function getUniqueSortedDates(data) {
  const uniqueDatesSet = new Set(data.map((entry) => entry.Day))
  const uniqueSortedDates = Array.from(uniqueDatesSet).sort((a, b) => {
    const dateA = new Date(a)
    const dateB = new Date(b)
    return dateA - dateB
  })

  return uniqueSortedDates
}

